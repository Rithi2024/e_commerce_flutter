create or replace function public.rpc_validate_promo_code(
  p_code text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user uuid := auth.uid();
  v_code text := upper(trim(coalesce(p_code, '')));
  v_subtotal numeric(10, 2) := 0;
  v_discount_percent numeric(5, 2) := 0;
  v_discount_amount numeric(10, 2) := 0;
  v_total numeric(10, 2) := 0;
  v_min_total numeric(10, 2) := 0;
begin
  if v_user is null then
    return jsonb_build_object(
      'valid', false,
      'message', 'Not authenticated',
      'code', '',
      'discount_percent', 0,
      'discount_amount', 0,
      'subtotal', 0,
      'total', 0,
      'min_order_total', 0
    );
  end if;

  select coalesce(sum(c.price * c.qty), 0)
  into v_subtotal
  from public.cart_items c
  where c.user_id = v_user;

  if v_code = '' then
    return jsonb_build_object(
      'valid', false,
      'message', 'Promo code is required',
      'code', '',
      'discount_percent', 0,
      'discount_amount', 0,
      'subtotal', v_subtotal,
      'total', v_subtotal,
      'min_order_total', 0
    );
  end if;

  select
    p.discount_percent,
    p.min_order_total
  into
    v_discount_percent,
    v_min_total
  from public.promo_codes p
  where upper(trim(p.code)) = v_code
    and p.is_active = true
    and (p.starts_at is null or p.starts_at <= now())
    and (p.expires_at is null or p.expires_at >= now())
  limit 1;

  if not found then
    return jsonb_build_object(
      'valid', false,
      'message', 'Promo code is invalid or expired',
      'code', v_code,
      'discount_percent', 0,
      'discount_amount', 0,
      'subtotal', v_subtotal,
      'total', v_subtotal,
      'min_order_total', 0
    );
  end if;

  if v_subtotal < coalesce(v_min_total, 0) then
    return jsonb_build_object(
      'valid', false,
      'message', format(
        'Promo code requires minimum order total of %s',
        to_char(v_min_total, 'FM999999990D00')
      ),
      'code', v_code,
      'discount_percent', v_discount_percent,
      'discount_amount', 0,
      'subtotal', v_subtotal,
      'total', v_subtotal,
      'min_order_total', v_min_total
    );
  end if;

  v_discount_amount := round(v_subtotal * (coalesce(v_discount_percent, 0) / 100.0), 2);
  v_discount_amount := greatest(0, least(v_discount_amount, v_subtotal));
  v_total := round(v_subtotal - v_discount_amount, 2);

  return jsonb_build_object(
    'valid', true,
    'message', 'Promo code applied',
    'code', v_code,
    'discount_percent', v_discount_percent,
    'discount_amount', v_discount_amount,
    'subtotal', v_subtotal,
    'total', v_total,
    'min_order_total', v_min_total
  );
end;
$$;

create or replace function public.rpc_place_order(
  p_address text default null,
  p_status text default 'order_received',
  p_delivery_type text default 'drop_off',
  p_address_details text default '',
  p_payment_method text default 'cash_on_delivery',
  p_payment_reference text default '',
  p_promo_code text default ''
)
returns bigint
language plpgsql
security invoker
set search_path = public
as $$
declare
  v_user uuid := auth.uid();
  v_items jsonb;
  v_subtotal numeric(10, 2);
  v_discount_amount numeric(10, 2) := 0;
  v_vat_amount numeric(10, 2) := 0;
  v_total numeric(10, 2);
  v_order_id bigint;
  v_address text;
  v_contact_phone text;
  v_address_details text := coalesce(trim(coalesce(p_address_details, '')), '');
  v_status text := coalesce(
    nullif(lower(trim(coalesce(p_status, ''))), ''),
    'order_received'
  );
  v_delivery_type text := coalesce(
    nullif(lower(trim(coalesce(p_delivery_type, ''))), ''),
    'drop_off'
  );
  v_payment_method text := coalesce(
    nullif(lower(trim(coalesce(p_payment_method, ''))), ''),
    'cash_on_delivery'
  );
  v_payment_reference text := coalesce(trim(coalesce(p_payment_reference, '')), '');
  v_promo_code text := upper(trim(coalesce(p_promo_code, '')));
  v_promo_discount_percent numeric(5, 2) := 0;
  v_promo_min_total numeric(10, 2) := 0;
  v_stock int;
  v_item record;
begin
  if v_user is null then
    raise exception 'Not authenticated';
  end if;

  if v_delivery_type not in ('drop_off', 'real_meeting') then
    raise exception 'Invalid delivery_type';
  end if;

  if v_payment_method not in ('cash_on_delivery', 'aba_payway_qr') then
    raise exception 'Invalid payment_method';
  end if;

  if v_status in ('pending', 'paid') then
    v_status := 'order_received';
  elsif v_status = 'shipped' then
    v_status := 'out_for_delivery';
  end if;

  select
    coalesce(
      jsonb_agg(
        jsonb_build_object(
          'productId', product_id,
          'name', name,
          'price', price,
          'imageUrl', image_url,
          'qty', qty,
          'size', size,
          'color', color
        )
      ),
      '[]'::jsonb
    ),
    coalesce(sum(price * qty), 0)
  into v_items, v_subtotal
  from public.cart_items
  where user_id = v_user;

  if jsonb_array_length(v_items) = 0 then
    raise exception 'Cart is empty';
  end if;

  select nullif(trim(p.phone), '')
  into v_contact_phone
  from public.profiles p
  where p.id = v_user;

  if v_contact_phone is null then
    raise exception 'Phone number is required';
  end if;

  if v_promo_code <> '' then
    select
      p.discount_percent,
      p.min_order_total
    into
      v_promo_discount_percent,
      v_promo_min_total
    from public.promo_codes p
    where upper(trim(p.code)) = v_promo_code
      and p.is_active = true
      and (p.starts_at is null or p.starts_at <= now())
      and (p.expires_at is null or p.expires_at >= now())
    limit 1;

    if not found then
      raise exception 'Invalid or expired promo code';
    end if;

    if v_subtotal < coalesce(v_promo_min_total, 0) then
      raise exception
        'Promo code requires minimum order total of %',
        to_char(v_promo_min_total, 'FM999999990D00');
    end if;

    v_discount_amount := round(v_subtotal * (coalesce(v_promo_discount_percent, 0) / 100.0), 2);
  end if;

  v_discount_amount := greatest(0, least(v_discount_amount, v_subtotal));
  v_vat_amount := round((v_subtotal - v_discount_amount) * 0.10, 2);
  v_vat_amount := greatest(0, v_vat_amount);
  v_total := round(v_subtotal - v_discount_amount + v_vat_amount, 2);

  v_address := nullif(trim(coalesce(p_address, '')), '');
  if v_address is null then
    select nullif(trim(address), '')
    into v_address
    from public.profiles
    where id = v_user;
  end if;

  if v_address is null then
    raise exception 'Address is required';
  end if;

  for v_item in
    select
      c.product_id,
      coalesce(c.size, '') as size,
      coalesce(c.color, '') as color,
      c.qty
    from public.cart_items c
    where c.user_id = v_user
  loop
    select s.stock
    into v_stock
    from public.product_variant_stocks s
    where s.product_id = v_item.product_id::uuid
      and s.size = v_item.size
      and s.color = v_item.color
    for update;

    if found and v_stock < v_item.qty then
      raise exception
        'Insufficient stock for product %, size %, color %',
        v_item.product_id,
        nullif(v_item.size, ''),
        nullif(v_item.color, '');
    end if;
  end loop;

  insert into public.orders (
    user_id,
    subtotal,
    discount_amount,
    vat_amount,
    promo_code,
    total,
    address,
    address_details,
    delivery_type,
    payment_method,
    payment_reference,
    status,
    items
  )
  values (
    v_user,
    v_subtotal,
    v_discount_amount,
    v_vat_amount,
    v_promo_code,
    v_total,
    v_address,
    v_address_details,
    v_delivery_type,
    v_payment_method,
    v_payment_reference,
    v_status,
    v_items
  )
  returning id into v_order_id;

  for v_item in
    select
      c.product_id,
      coalesce(c.size, '') as size,
      coalesce(c.color, '') as color,
      c.qty
    from public.cart_items c
    where c.user_id = v_user
  loop
    update public.product_variant_stocks s
    set stock = s.stock - v_item.qty
    where s.product_id = v_item.product_id::uuid
      and s.size = v_item.size
      and s.color = v_item.color;
  end loop;

  delete from public.cart_items where user_id = v_user;

  return v_order_id;
end;
$$;

