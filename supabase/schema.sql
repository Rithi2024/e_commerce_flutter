create or replace function public.guard_profile_account_type_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if new.account_type is distinct from old.account_type then
    if auth.uid() is not null and not public.is_super_admin(auth.uid()) then
      raise exception 'Only super admins can change account type';
    end if;
  end if;
  return new;
end;
$$;

