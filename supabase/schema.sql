create or replace function public.rpc_staff_update_order_status(
  p_order_id bigint,
  p_status text
)
returns public.orders
language plpgsql
security definer
set search_path = public
as $$
declare
  v_status text := lower(trim(coalesce(p_status, '')));
  v_order public.orders;
  v_current_status text;
begin
  if not public.is_staff(auth.uid()) then
    raise exception 'Staff only';
  end if;

  if p_order_id is null then
    raise exception 'Order id is required';
  end if;

  if v_status in ('pending', 'paid') then
    v_status := 'order_received';
  elsif v_status = 'shipped' then
    v_status := 'out_for_delivery';
  end if;

  if v_status not in (
    'order_received',
    'order_packed',
    'ready_for_pickup',
    'out_for_delivery',
    'delivered',
    'cancelled'
  ) then
    raise exception 'Invalid status';
  end if;

  if public.is_rider(auth.uid()) and not public.is_admin(auth.uid())
    and v_status not in ('out_for_delivery', 'delivered') then
    raise exception 'Rider can update only out_for_delivery or delivered';
  end if;

  select *
  into v_order
  from public.orders o
  where o.id = p_order_id
  for update;

  if v_order.id is null then
    raise exception 'Order not found';
  end if;

  if v_order.status = 'cancelled' then
    raise exception 'Cancelled order cannot be updated';
  end if;

  v_current_status := lower(trim(coalesce(v_order.status, '')));
  if v_current_status in ('pending', 'paid') then
    v_current_status := 'order_received';
  elsif v_current_status = 'shipped' then
    v_current_status := 'out_for_delivery';
  end if;

  if public.is_rider(auth.uid()) and not public.is_admin(auth.uid()) then
    if v_status = 'out_for_delivery' and v_current_status not in ('order_received', 'order_packed') then
      raise exception 'Rider can mark out_for_delivery only from order_received or order_packed';
    end if;
    if v_status = 'delivered' and v_current_status not in ('out_for_delivery', 'ready_for_pickup') then
      raise exception 'Rider can mark delivered only after out_for_delivery or ready_for_pickup';
    end if;
  end if;

  if v_current_status = v_status then
    return v_order;
  end if;

  update public.orders o
  set status = v_status
  where o.id = p_order_id
  returning * into v_order;

  return v_order;
end;
$$;

