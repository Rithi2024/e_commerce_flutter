create or replace function public.rpc_upsert_payway_transaction(
  p_tran_id text,
  p_order_id bigint default null,
  p_amount numeric default null,
  p_currency text default 'USD',
  p_check_response jsonb default '{}'::jsonb
)
returns bigint
language plpgsql
security invoker
set search_path = public
as $$
declare
  v_user uuid := auth.uid();
  v_txn_id bigint;
  v_status text := upper(
    coalesce(
      nullif(trim(coalesce(
        p_check_response -> 'data' ->> 'payment_status',
        p_check_response ->> 'payment_status'
      )), ''),
      ''
    )
  );
  v_status_code int := null;
  v_gateway_code text := coalesce(
    nullif(trim(coalesce(
      p_check_response -> 'status' ->> 'code',
      p_check_response -> 'data' -> 'status' ->> 'code',
      p_check_response ->> 'code'
    )), ''),
    ''
  );
  v_gateway_message text := coalesce(
    nullif(trim(coalesce(
      p_check_response -> 'status' ->> 'message',
      p_check_response -> 'data' -> 'status' ->> 'message',
      p_check_response ->> 'message'
    )), ''),
    ''
  );
  v_currency text := upper(coalesce(nullif(trim(p_currency), ''), 'USD'));
  v_amount numeric(10, 2) := coalesce(p_amount, 0)::numeric(10, 2);
  v_status_code_text text := coalesce(
    nullif(trim(coalesce(
      p_check_response -> 'data' ->> 'payment_status_code',
      p_check_response ->> 'payment_status_code'
    )), ''),
    ''
  );
begin
  if v_user is null then
    raise exception 'Not authenticated';
  end if;

  if coalesce(trim(p_tran_id), '') = '' then
    raise exception 'tran_id is required';
  end if;

  if v_status_code_text ~ '^-?[0-9]+$' then
    v_status_code := v_status_code_text::int;
  end if;

  insert into public.payway_transactions (
    user_id,
    order_id,
    provider,
    tran_id,
    amount,
    currency,
    payment_status,
    payment_status_code,
    gateway_status_code,
    gateway_message,
    raw_response
  )
  values (
    v_user,
    p_order_id,
    'payway',
    trim(p_tran_id),
    v_amount,
    v_currency,
    v_status,
    v_status_code,
    v_gateway_code,
    v_gateway_message,
    coalesce(p_check_response, '{}'::jsonb)
  )
  on conflict (provider, tran_id)
  do update
    set
      user_id = excluded.user_id,
      order_id = coalesce(excluded.order_id, public.payway_transactions.order_id),
      amount = excluded.amount,
      currency = excluded.currency,
      payment_status = case
        when excluded.payment_status = '' then public.payway_transactions.payment_status
        else excluded.payment_status
      end,
      payment_status_code = coalesce(
        excluded.payment_status_code,
        public.payway_transactions.payment_status_code
      ),
      gateway_status_code = case
        when excluded.gateway_status_code = '' then public.payway_transactions.gateway_status_code
        else excluded.gateway_status_code
      end,
      gateway_message = case
        when excluded.gateway_message = '' then public.payway_transactions.gateway_message
        else excluded.gateway_message
      end,
      raw_response = case
        when excluded.raw_response = '{}'::jsonb then public.payway_transactions.raw_response
        else excluded.raw_response
      end,
      updated_at = now()
  returning id into v_txn_id;

  return v_txn_id;
end;
$$;

