)
returns table (product_id text)
language sql
stable
security definer
set search_path = public
as $$
  with sales as (
    select
      nullif(
        trim(
          coalesce(
            item.value->>'productId',
            item.value->>'product_id',
            ''
          )
        ),
        ''
      ) as product_id,
      case
        when coalesce(item.value->>'qty', item.value->>'quantity', '') ~ '^\s*\d+\s*$'
          then greatest(
            trim(coalesce(item.value->>'qty', item.value->>'quantity'))::int,
            1
          )
        else 1
      end as qty
    from public.orders o
    cross join lateral jsonb_array_elements(coalesce(o.items, '[]'::jsonb)) as item
    where coalesce(lower(trim(o.status)), '') <> 'cancelled'
      and o.created_at >=
        now() - make_interval(days => greatest(coalesce(p_days, 30), 1))
  )
  select s.product_id
  from sales s
  where s.product_id is not null
  group by s.product_id
  order by sum(s.qty) desc, s.product_id asc
  limit greatest(coalesce(p_limit, 5), 1);
$$;

