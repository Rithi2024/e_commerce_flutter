create or replace function public.has_purchased_product(
  p_user_id uuid,
  p_product_id text
)
returns boolean
language sql
stable
security invoker
set search_path = public
as $$
  select
    p_user_id is not null
    and nullif(trim(coalesce(p_product_id, '')), '') is not null
    and exists (
      select 1
      from public.orders o
      cross join lateral jsonb_array_elements(
        coalesce(o.items, '[]'::jsonb)
      ) as item
      where o.user_id = p_user_id
        and coalesce(lower(trim(o.status)), '') <> 'cancelled'
        and coalesce(
          item.value->>'productId',
          item.value->>'product_id',
          ''
        ) = trim(p_product_id)
    );
$$;

