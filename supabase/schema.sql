create or replace function public.rpc_add_to_cart(
  p_product_id text,
  p_name text,
  p_price numeric,
  p_image_url text default '',
  p_size text default null,
  p_color text default null,
  p_qty int default 1
)
returns public.cart_items
language plpgsql
security invoker
set search_path = public
as $$
declare
  v_user uuid := auth.uid();
  v_qty int := greatest(coalesce(p_qty, 1), 1);
  v_size text := coalesce(nullif(trim(coalesce(p_size, '')), ''), '');
  v_color text := coalesce(nullif(trim(coalesce(p_color, '')), ''), '');
  v_cart_key text := public.make_cart_key(p_product_id, v_size, v_color);
  v_product_uuid uuid := p_product_id::uuid;
  v_stock int;
  v_existing_qty int := 0;
  v_row public.cart_items;
begin
  if v_user is null then
    raise exception 'Not authenticated';
  end if;

  select s.stock
  into v_stock
  from public.product_variant_stocks s
  where s.product_id = v_product_uuid
    and s.size = v_size
    and s.color = v_color
  for update;

  if found then
    if v_stock <= 0 then
      raise exception 'Out of stock';
    end if;

    select coalesce(c.qty, 0)
    into v_existing_qty
    from public.cart_items c
    where c.user_id = v_user and c.cart_key = v_cart_key;

    if v_existing_qty + v_qty > v_stock then
      raise exception 'Only % item(s) available for this size/color', v_stock;
    end if;
  end if;

  insert into public.cart_items (
    user_id, cart_key, product_id, name, price, image_url, qty, size, color
  )
  values (
    v_user,
    v_cart_key,
    p_product_id,
    p_name,
    p_price,
    coalesce(p_image_url, ''),
    v_qty,
    nullif(v_size, ''),
    nullif(v_color, '')
  )
  on conflict (user_id, cart_key) do update
    set qty = public.cart_items.qty + excluded.qty,
        name = excluded.name,
        price = excluded.price,
        image_url = excluded.image_url,
        size = excluded.size,
        color = excluded.color
  returning * into v_row;

  return v_row;
end;
$$;

