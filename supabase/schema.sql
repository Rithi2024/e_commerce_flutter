select
  p.id,
  sizes.size,
  colors.color,
  20
from public.products p
cross join (values ('S'), ('M'), ('L'), ('XL')) as sizes(size)
cross join (values ('Black'), ('White'), ('Blue'), ('Red')) as colors(color)
