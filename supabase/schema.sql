create or replace function public.rpc_admin_create_event(
  p_title text,
  p_subtitle text default '',
  p_badge text default '',
  p_is_active boolean default false,
  p_starts_at timestamptz default null,
  p_expires_at timestamptz default null,
  p_theme text default 'default'
)
returns public.events
language plpgsql
security definer
set search_path = public
as $$
declare
  v_row public.events;
  v_now timestamptz := now();
  v_starts timestamptz := coalesce(p_starts_at, v_now);
  v_expires timestamptz := coalesce(p_expires_at, v_starts + interval '24 hours');
  v_theme text := lower(trim(coalesce(p_theme, 'default')));
begin
  if not public.is_admin(auth.uid()) then
    raise exception 'Admin only';
  end if;

  if coalesce(trim(p_title), '') = '' then
    raise exception 'Title is required';
  end if;

  if v_theme = '' then
    v_theme := 'default';
  end if;

  if v_theme not in (
    'default',
    'christmas_sale',
    'valentine',
    'new_year',
    'black_friday',
    'summer_sale'
  ) then
    raise exception 'Invalid event theme';
  end if;

  if v_expires <= v_starts then
    raise exception 'Event end must be after start';
  end if;

  if coalesce(p_is_active, false) and v_expires <= v_now then
    raise exception 'Active event must end in the future';
  end if;

  if coalesce(p_is_active, false) then
    update public.events set is_active = false where is_active = true;
  end if;

  insert into public.events (
    title,
    subtitle,
    badge,
    is_active,
    starts_at,
    expires_at,
    theme
  )
  values (
    trim(p_title),
    coalesce(p_subtitle, ''),
    coalesce(p_badge, ''),
    coalesce(p_is_active, false),
    v_starts,
    v_expires,
    v_theme
  )
  returning * into v_row;

  return v_row;
end;
$$;

drop function if exists public.rpc_admin_update_event(uuid, text, text, text, boolean);
drop function if exists public.rpc_admin_update_event(uuid, text, text, text, boolean, timestamptz);
create or replace function public.rpc_admin_update_event(
  p_event_id uuid,
  p_title text,
  p_subtitle text default '',
  p_badge text default '',
  p_is_active boolean default false,
  p_starts_at timestamptz default null,
  p_expires_at timestamptz default null,
  p_theme text default null
)
returns public.events
language plpgsql
security definer
set search_path = public
as $$
declare
  v_row public.events;
  v_now timestamptz := now();
  v_existing_starts timestamptz;
  v_existing_expiry timestamptz;
  v_existing_theme text;
  v_starts timestamptz;
  v_expires timestamptz;
  v_theme text;
begin
  if not public.is_admin(auth.uid()) then
    raise exception 'Admin only';
  end if;

  if coalesce(trim(p_title), '') = '' then
    raise exception 'Title is required';
  end if;

  select
    e.starts_at,
    e.expires_at,
    e.theme
  into
    v_existing_starts,
    v_existing_expiry,
    v_existing_theme
  from public.events e
  where e.id = p_event_id;

  if v_existing_expiry is null or v_existing_starts is null then
    raise exception 'Event not found';
  end if;

  v_starts := coalesce(p_starts_at, v_existing_starts);
  v_expires := coalesce(p_expires_at, v_existing_expiry);
  v_theme := lower(trim(coalesce(p_theme, v_existing_theme, 'default')));

  if v_theme = '' then
    v_theme := 'default';
  end if;

  if v_theme not in (
    'default',
    'christmas_sale',
    'valentine',
    'new_year',
    'black_friday',
    'summer_sale'
  ) then
    raise exception 'Invalid event theme';
  end if;

  if v_expires <= v_starts then
    raise exception 'Event end must be after start';
  end if;

  if coalesce(p_is_active, false) and v_expires <= v_now then
    raise exception 'Active event must end in the future';
  end if;

  if coalesce(p_is_active, false) then
    update public.events
    set is_active = false
    where is_active = true and id <> p_event_id;
  end if;

  update public.events
  set
    title = trim(p_title),
    subtitle = coalesce(p_subtitle, ''),
    badge = coalesce(p_badge, ''),
    is_active = coalesce(p_is_active, false),
    starts_at = v_starts,
    theme = v_theme,
    expires_at = v_expires
  where id = p_event_id
  returning * into v_row;

  if v_row.id is null then
    raise exception 'Event not found';
  end if;

  return v_row;
end;
$$;

create or replace function public.rpc_admin_delete_event(
  p_event_id uuid
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  v_deleted integer := 0;
begin
  if not public.is_admin(auth.uid()) then
    raise exception 'Admin only';
  end if;

  delete from public.events where id = p_event_id;
  get diagnostics v_deleted = row_count;
  return v_deleted > 0;
end;
$$;

